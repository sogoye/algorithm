#include <stdio.h>

void quicksort(int array[], int left, int right)
{
	int i = left;
	int j = right;
	//int pivot = array[(left + right) / 2];
	int pivot = array[right];

	while (i <= j)
	{
		while (array[i] < pivot)
		{
			i++;
		}
		while (array[j] > pivot)
		{
			j--;
		}

		if (i <= j)
		{
			int temp = array[i];
		    array[i] = array[j];
			array[j] = temp;
			i++;
			j--;
		}
	}

	if (left < j)
	{
		quicksort(array, left ,j);
	}
	if (i < right)
	{
		quicksort(array, i ,right);
	}
}

